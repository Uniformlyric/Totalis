interface SidebarProps {
  collapsed: boolean;
  onToggle: () => void;
  mobileOpen: boolean;
  onMobileClose: () => void;
}

export function Sidebar({ collapsed, onToggle, mobileOpen, onMobileClose }: SidebarProps) {
  const currentPath = typeof window !== 'undefined' ? window.location.pathname : '/';

  const isActive = (href: string) => {
    if (href === '/') return currentPath === '/';
    return currentPath.startsWith(href);
  };

  return (
    <aside
      className={`
        fixed top-0 left-0 z-50 h-full bg-surface border-r border-border
        transition-all duration-300
        ${collapsed ? 'w-20' : 'w-64'}
        ${mobileOpen ? 'translate-x-0' : '-translate-x-full lg:translate-x-0'}
      `}
    >
      <div className="flex flex-col h-full">
        {/* Logo */}
        <div className="flex items-center justify-between h-16 px-4 border-b border-border">
          <a href="/" className="flex items-center gap-3">
            <div className="w-8 h-8 bg-gradient-to-br from-primary to-secondary rounded-lg flex items-center justify-center">
              <span className="text-white font-bold text-sm">T</span>
            </div>
            {!collapsed && (
              <span className="text-lg font-bold text-text">Totalis</span>
            )}
          </a>
          
          {/* Collapse button - desktop only */}
          <button
            onClick={onToggle}
            className="hidden lg:flex p-1.5 text-text-muted hover:text-text hover:bg-surface-hover rounded-lg transition-colors"
            aria-label={collapsed ? 'Expand sidebar' : 'Collapse sidebar'}
          >
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="18"
              height="18"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
              className={`transition-transform ${collapsed ? 'rotate-180' : ''}`}
            >
              <polyline points="15 18 9 12 15 6" />
            </svg>
          </button>

          {/* Close button - mobile only */}
          <button
            onClick={onMobileClose}
            className="lg:hidden p-1.5 text-text-muted hover:text-text hover:bg-surface-hover rounded-lg transition-colors"
            aria-label="Close sidebar"
          >
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="18"
              height="18"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
            >
              <line x1="18" y1="6" x2="6" y2="18" />
              <line x1="6" y1="6" x2="18" y2="18" />
            </svg>
          </button>
        </div>

        {/* Navigation */}
        <nav className="flex-1 overflow-y-auto py-4 px-3">
          <ul className="space-y-1">
            {navItems.map((item) => (
              <li key={item.name}>
                <a
                  href={item.href}
                  onClick={onMobileClose}
                  className={`
                    flex items-center gap-3 px-3 py-2.5 rounded-lg
                    transition-colors
                    ${
                      isActive(item.href)
                        ? 'bg-primary text-white'
                        : 'text-text-secondary hover:text-text hover:bg-surface-hover'
                    }
                    ${collapsed ? 'justify-center' : ''}
                  `}
                  title={collapsed ? item.name : undefined}
                >
                  {item.icon}
                  {!collapsed && <span className="font-medium">{item.name}</span>}
                </a>
              </li>
            ))}
          </ul>
        </nav>

        {/* Bottom navigation */}
        <div className="py-4 px-3 border-t border-border">
          <ul className="space-y-1">
            {bottomNavItems.map((item) => (
              <li key={item.name}>
                <a
                  href={item.href}
                  onClick={onMobileClose}
                  className={`
                    flex items-center gap-3 px-3 py-2.5 rounded-lg
                    transition-colors
                    ${
                      isActive(item.href)
                        ? 'bg-primary text-white'
                        : 'text-text-secondary hover:text-text hover:bg-surface-hover'
                    }
                    ${collapsed ? 'justify-center' : ''}
                  `}
                  title={collapsed ? item.name : undefined}
                >
                  {item.icon}
                  {!collapsed && <span className="font-medium">{item.name}</span>}
                </a>
              </li>
            ))}
          </ul>
        </div>
      </div>
    </aside>
  );
}
